const mongoose=require('mongoose');
const favourite=new mongoose.Schema({
    email:{type:String,
        required:true,
        unique:true,
        match:/[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/
    },
    id:[{type:String}]
});
module.exports=Favourite=mongoose.model('favourite',favourite);